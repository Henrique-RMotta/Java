    private void adicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarActionPerformed
      
// TODO add your handling code here:
        double Total = ((Produtos)listaItens.getSelectedItem())
                .getPreco() * 
                Integer.parseInt(jSpinner1
                .getValue()
                .toString());
        tTexto.append(
        jSpinner1.getValue().toString()
        + "x"
        + listaItens
        .getSelectedItem()
        .toString()
        + "="
        + Total);
        tTexto.append("\n");
        
        produtoVenda pVenda = new produtoVenda();
        pVenda.setProduto((Produtos) listaItens.getSelectedItem());
        pVenda.setQuantidade(Integer.parseInt(jSpinner1.getValue().toString()));
        pVenda.setTotal();
        total();
        
        int quantidade = Integer.parseInt(
                         jSpinner1.getValue().toString());
        Produtos produto = (Produtos)listaItens.getSelectedItem();
        if (produto.getSaldoEstoque() >= quantidade){
            produtoVenda p = new produtoVenda();
            p.setProduto(produto);
            p.setQuantidade(quantidade);
            p.setTotal();
            listaCarrinho.addElement(pVenda);
           
        }
         atualizaSaldo();
     
    }//GEN-LAST:event_adicionarActionPerformed
    public void total () {
        double total =0; 
        for (int i =0 ; i< listaCarrinho.getSize(); i++) {
           total += listaCarrinho.getElementAt(i).getTotal();
           tTotal.setText("Total:" + total);
        }
    }
    public void atualizaSaldo() {
        tSaldo.setText(Integer.toString(((Produtos)listaItens.getSelectedItem()).getSaldoEstoque()));
    }
    private void bRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRemoveActionPerformed
        // TODO add your handling code here:
        produtoVenda p; 
        if (Carrinho.getSelectedIndex() >=0) {
        p = listaCarrinho.remove(
        listaItens.getSelectedIndex());
        
        p.getProduto().setSaldoEstoque(
                p.getProduto().getSaldoEstoque() + p.getQuantidade());
        atualizaSaldo();
             total();
        }
    }//GEN-LAST:event_bRemoveActionPerformed

